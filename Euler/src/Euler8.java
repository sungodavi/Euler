	public static long solve(String s)
	{
		long ans = 0;
		long temp = 1;
		for(int x = 0; x < s.length() - 13; x++)
		{
			temp = 1;
			for(int y = x; y < x + 13; y++)
				temp *= Character.getNumericValue(s.charAt(y));
			
			if(ans < temp)
				ans = temp;
		}
		return ans;
	}
	
	
